#include "examtest.h"

void Exametest::sort(LNode* La, LNode* Lb)
{
	auto q=La->next;
	auto p = La;
	auto r = Lb;
	
	while (q)
	{
		if (q->data%2==0)
		{
			
			q->next = q->next->next;
			r->next = p;
			r = r->next;
			p - q->next;
		}
		else
		{
			p = p->next;
			q = q ->next;
		}
	}
}
